    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String montant = tf_montant_retrait.getText().toString();
        if (combo_retrait.getSelectedIndex() != 0 && !montant.equals("")) {
            Client client = dao.searchByID(combo_retrait.getSelectedItem().toString());
            Client cl = new Client();
            cl.setIdClient(client.getIdClient());
            Long solde = client.getSolde();

            Retrait retrait = new Retrait();
            retrait.setClient(cl);
            retrait.setMontant(Long.valueOf(montant));
            retrait.setDate(new Date());
            if (jButton7.getText().toString().equalsIgnoreCase("Ajouter")) {
                if (Long.valueOf(montant) > solde) {
                    JOptionPane.showMessageDialog(this, "Votre solde est insuffissant");
                } else {
                    dao_retrait.addRetrait(retrait);
                    retrait_effacer();
                    client_table("", "");
                    retrait_table();
                }

            } else {
                Retrait retrait_ = dao_retrait.searchByID(Long.valueOf(
                        tf_id_retrait.getText()
                        .toString()));
                Long solde_nouv = solde + retrait_.getMontant();
                if (solde_nouv < Long.valueOf(montant)) {
                    JOptionPane.showMessageDialog(this, "Votre solde est insuffissant");
                } else {
                    retrait.setIdRetrait(Long.valueOf(tf_id_retrait.getText()
                            .toString()));
                    dao_retrait.updateRetrait(retrait);
                    retrait_effacer();
                    client_table("", "");
                    retrait_table();
                }

            }


        } else {
            JOptionPane.showMessageDialog(this, "Veuillez remplir les champs vides");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

