    public void client_table(String search, String choix) {
        dao = new ClientImpl();
        try {
            dt = new DefaultTableModel();
            dt.addColumn("Numero");
            dt.addColumn("Nom");
            dt.addColumn("Solde");
            tableau_client.setModel(dt);
            dt.setRowCount(0);
            List<Client> clients = new ArrayList<>();
            if (search.isEmpty()) {
                clients = dao.getAllClient();
            } else {
                clients = dao.getAllClientSearch(search, choix);
            }
            for (Client object : clients) {
                Object[] affiche = {object.getIdClient(), object.getNomClient(), object.getSolde()};
                dt.addRow(affiche);
                tableau_client.setModel(dt);
            }
            nbr_effectifs.setText(String.valueOf(dao.CompteClient()));
            chargementCombo();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void chargementCombo() {
        dao = new ClientImpl();
        combo_versement.removeAllItems();
        combo_retrait.removeAllItems();
        //combo_etat.removeAllItems();
        Iterator it = dao.getAllClient().iterator();
        combo_versement.addItem("--------CHOIX--------");
        combo_retrait.addItem("--------CHOIX--------");
        //combo_etat.addItem("--------CHOIX--------");
        while (it.hasNext()) {
            Client client = (Client) it.next();
            combo_versement.addItem(client.getNomClient());
            combo_retrait.addItem(client.getNomClient());
            //combo_etat.addItem(client.getNomClient());
        }
        chargementCombo_();
    }

