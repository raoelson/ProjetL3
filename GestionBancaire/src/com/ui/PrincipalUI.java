    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        dao = new ClientImpl();
        String nom = tfnom_client.getText().toString().toUpperCase();
        String solde = tfsolde_client.getText().toString();
        if (!nom.equals("") && !solde.equals("")) {
            Client client = new Client();
            client.setNomClient(nom);
            client.setSolde(Long.valueOf(solde));
            if (jButton1.getText().toString().equalsIgnoreCase("Ajouter")) {
                dao.addClient(client);
            } else {
                client.setIdClient(Integer.parseInt(tfid_client.getText().toString()));
                dao.updateClient(client);
            }
            client_effacer();
            client_table("", "");
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez remplir les champs vides");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

