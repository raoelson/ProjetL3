    public void etat_table(int id) {
        int somme_versement = 0;
        int somme_retrait = 0;
        try {
            dt = new DefaultTableModel();
            dt.addColumn("N° compte");
            dt.addColumn("Versement");
            dt.addColumn("Retrait");
            dt.addColumn("Date");
            jTable1.setModel(dt);
            dt.setRowCount(0);
            dao_retrait = new RetraitImpl();
            dao_versement = new VersementImpl();
//          
            List<Retrait> retraits = new ArrayList<>();
            retraits = dao_retrait.getSearch(id);
            for (Retrait object : retraits) {
                somme_retrait += object.getMontant();
                Object[] affiche = {object.getIdRetrait(), "", object.getMontant(), object.getDate()};
                dt.addRow(affiche);
                jTable1.setModel(dt);
            }
            List<Versement> versements = new ArrayList<>();
            versements = dao_versement.getsearch(id);
            for (Versement object : versements) {
                somme_versement += object.getMontant();
                Object[] affiche = {object.getIdVersement(), object.getMontant(), "", object.getDate()};
                dt.addRow(affiche);
                jTable1.setModel(dt);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        somme_global_verser = somme_versement;
        somme_global_retirer = somme_retrait;
        jLabel23.setText(String.valueOf(somme_versement));
        jLabel25.setText(String.valueOf(somme_retrait));
    }
