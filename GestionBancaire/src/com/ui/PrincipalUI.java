    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String montant = tf_montant_vers.getText().toString();
        if (combo_versement.getSelectedIndex() != 0 && !montant.equals("")) {
            Date date = new Date();
            final SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
            final String dateStr = sdf.format(new Date());
            dao = new ClientImpl();
            dao_versement = new VersementImpl();
            Client client = dao.searchByID(combo_versement.getSelectedItem().toString());
            Client cl = new Client();
            cl.setIdClient(client.getIdClient());
            Versement versement = new Versement();
            versement.setClient(cl);
            versement.setMontant(Long.valueOf(montant));
            versement.setDate((new Date()));
            versement.setAnnee(Integer.parseInt(dateStr.substring(6, 10)));
            versement.setMois(Integer.parseInt(dateStr.substring(3, 5)));
            if (jButton4.getText().toString().equals("Ajouter")) {
                dao_versement.addVersement(versement);
            } else {
                versement.setIdVersement(Long.valueOf(tf_id_vers.getText().toString()));
                dao_versement.updateVersement(versement);
            }
            client_table("", "");
            versement_table();
            versement_effacer();

        } else {
            JOptionPane.showMessageDialog(this, "Veuillez remplir les champs vides");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

